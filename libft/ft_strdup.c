#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*result;
	int		i;

	i = ft_strlen(src);
	result = malloc(sizeof(char) * (i + 1));
	if (!result)
		return (0);
	i = 0;
	while (src[i])
	{
		result[i] = src[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
